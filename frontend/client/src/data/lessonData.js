// Helper functions for data manipulation
export const getSubjects = () => {
  return [...new Set(lessonData.map((lesson) => lesson.subject))];
};

export const getLevels = () => {
  return [...new Set(lessonData.map((lesson) => lesson.level))];
};

export const getAllTags = () => {
  const allTags = lessonData.flatMap((lesson) => lesson.tags);
  return [...new Set(allTags)];
};

export const getLessonsBySubject = (subject) => {
  return lessonData.filter((lesson) => lesson.subject === subject);
};

export const getLessonsByLevel = (level) => {
  return lessonData.filter((lesson) => lesson.level === level);
};

export const getLessonsByAuthor = (author) => {
  return lessonData.filter((lesson) => lesson.author === author);
};

export const getPopularLessons = (limit = 5) => {
  return lessonData
    .sort((a, b) => b.likes + b.downloads - (a.likes + a.downloads))
    .slice(0, limit);
};

export const getRecentLessons = (limit = 5) => {
  return lessonData
    .sort((a, b) => new Date(b.uploadDate) - new Date(a.uploadDate))
    .slice(0, limit);
};
